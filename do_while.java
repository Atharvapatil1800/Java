public class do_while {   // Define main class
        public static void main(String[] args) {  // The main method where the program starts
            int i=1;    // Decleration of integer i with value 1
            do {  // Strt of do-while loop
                System.out.println(i);  // Print thevalue of i
                i++;   // Incremnt of i by 1
            } while (i<=100);  // Continue the loop as i is less than or equal to 100
        }


}
